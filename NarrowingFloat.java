class  NarrowingFloat
{
	public static void main(String[] args) 
	{
		float a=24.445664f;
		long c=(long)a;
		int d=(int)a;
		short e=(short)a;
		char f=(char)a;
		byte g=(byte)a;
		System.out.println(a);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);


	}
}