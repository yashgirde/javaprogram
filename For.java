class For
{
	public static void main(String[] args) {
		
		for (int i=1; i<=10; i++) {

		
		System.out.println(i);
	    
	    }
        
        for (char ch = 'a'; ch<='z'; ch++ ) {
        	

        System.out.println(ch);

        }

        for (char bc = 'z'; bc>='a'; bc-- ) {

        System.out.println(bc);
        	
        }
         
        for (int a = 0; a<=9; a++ ) {


        System.out.println(a);
        	
        }

     }
}


