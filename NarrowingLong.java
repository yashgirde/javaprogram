class  NarrowingLong
{
	public static void main(String[] args) 
	{
		long a=8459309278l;
		int d=(int)a;
		short e=(short)a;
		char f=(char)a;
		byte g=(byte)a;
		System.out.println(a);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);


	}
}